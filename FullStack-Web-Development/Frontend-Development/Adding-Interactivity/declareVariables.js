function declareVariables() {
  // Example: Variable Declaration
  var myName = "Alice"; // Declaring a variable using 'var' -> Function/global scope
  let age = 25; // Declaring a variable using 'let' -> Block-scoped
  const country = "USA"; // Declaring a variable using 'const' -> Block-scoped & cannot be reassigned
  console.log("Name: " + myName + ", Age: " + age + ", Country: " + country);
}