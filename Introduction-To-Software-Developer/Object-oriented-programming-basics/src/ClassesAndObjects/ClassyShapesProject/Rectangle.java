package ClassesAndObjects.ClassyShapesProject;

public class Rectangle {
    // member variables to store the length and width of a rectangle
    double length;
    double width;

    public Rectangle(){
        this.length = 0.0;
        this.width = 0.0;
    }

    public Rectangle(double length, double width){
        this.length = length;
        this.width = width;
    }
}
